package practise;

public class Data_Types {
	public static void main(String args[]) {
		int a = 10;
		float b = 10.0f;
		double d = 100.88;
		char x = 'a';
		String s = "HHello World";
		System.out.println(a);
		System.out.println(b);
		System.out.println(d);
		System.out.println(x);
		System.out.println(s);
	}
}
